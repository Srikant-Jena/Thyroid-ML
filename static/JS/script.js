// TSH
function toggleTSHVisibility() {
    var element = document.getElementById('ifTSHTrue');
    var checkbox = document.getElementById('TrueTSHCheck');
    element.style.visibility = checkbox.checked ? 'visible' : 'hidden';
  }
  
